def _assert_query(result, qi):
    sr = result.get("search_results", [])
    assert qi < len(sr), f"search_results에 쿼리 {qi + 1} 결과 없음"
    sub = sr[qi].get("top3", [])
    ref = EXPECTED_QUERY_RESULTS[qi]
    sub_idx = [x["doc_index"] for x in sub]
    ref_idx = [x["doc_index"] for x in ref]
    assert sub_idx == ref_idx, f"doc_index 불일치: {sub_idx} != {ref_idx}"
    for s, r in zip(sub, ref):
        assert abs(round(s.get("similarity", 0), 6) - r["similarity"]) < 1e-3, (
            f"similarity 불일치: {s.get('similarity')} != {r['similarity']}"
        )
